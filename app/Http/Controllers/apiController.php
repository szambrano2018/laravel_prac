    {
        $campo1      =  $request->input('campo1');
        $campo2      =  $request->input('campo2');
        $comentarios =  $request->input('comentarios');
        
        DB::table('tabla_maestro')->insert(
                    array('campo1'      => $campo1,
                          'campo2'      => $campo2,
                          'comentarios' => $comentarios,
                 )
        );
        return response()->json(['status'=>true,'Exitoso'],200);

